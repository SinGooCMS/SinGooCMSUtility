        /// <summary>
        /// 前缀集合
        /// </summary>
        public static List<string> Prefix
        {
            get
            {
                if (_prefix == null)
                {
                    lock (typeof(CellPhonePrefix))
                    {
                        if (_prefix == null)
                        {
                            _prefix = Dictionary.Select(i => i.prefix).Distinct().ToList();
                        }
                    }
                }
                return _prefix;
            }
